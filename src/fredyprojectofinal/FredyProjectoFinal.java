    private void standActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_standActionPerformed
        // TODO add your handling code here:
        
        deaer += rand.nextInt(5,11);
        bljkUpd();
        if (deaer == 21){
            updPlr();
            hit.setVisible(false);
            stand.setVisible(false);
        }
        else if(plaer == 21){
            plr.setChips(plr.getChips() + (moneybet * 3));
            updPlr();
            hit.setVisible(false);
            stand.setVisible(false);
        }
        else if(deaer > 21 && plaer < 21){
            plr.setChips(plr.getChips() + moneybet);
            updPlr();
            hit.setVisible(false);
            stand.setVisible(false);
        }
        else if(plaer > deaer){
            plr.setChips(plr.getChips() + moneybet);
            updPlr();
            hit.setVisible(false);
            stand.setVisible(false);
        }
        else{
            updPlr();
            hit.setVisible(false);
            stand.setVisible(false);
        }
    }//GEN-LAST:event_standActionPerformed

    private void rollActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rollActionPerformed
        // TODO add your handling code here:
        
        if (Integer.parseInt(betText.getText()) < plr.getDinero()){
            
            plr.setDinero(plr.getDinero() - moneybet);
            
            int roll1 = rand.nextInt(0,12);
            int roll2 = rand.nextInt(0,12);   
            int roll3 = rand.nextInt(0,12);
            
            slot1.setText(Integer.toString(roll1));
            slot2.setText(Integer.toString(roll2));
            slot3.setText(Integer.toString(roll3));
            
            if(roll1 == 7 && roll2 == 7 && roll3 == 7 ){
                plr.setChips(plr.getChips() + (moneybet * 7));
            }
            else if (roll1 == roll2 || roll2 == roll3 || roll3 == roll1){
                plr.setChips(plr.getChips() + (moneybet * 3));
            }
            updPlr();
        }
        else{
            roll.setVisible(false);
            updPlr();
        }
        
        
    }//GEN-LAST:event_rollActionPerformed

