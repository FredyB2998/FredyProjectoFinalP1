    private void pagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pagarActionPerformed
        // TODO add your handling code here:
        if (cantPagar.getText().equalsIgnoreCase("cheat")){
            plr.setDinero(9999);
            plr.setChips(300);
            updPlr();
        }
        else{
            plr.setDeuda(plr.getDeuda()- Integer.parseInt(cantPagar.getText()));
            plr.setDinero(plr.getDinero()- Integer.parseInt(cantPagar.getText()));

            cantPagar.setVisible(false);
            pagar.setVisible(false);

            if (plr.getDeuda() <= 0 ){
                plr.setDeuda(0);
                salir.setVisible(true);
                endText.setVisible(true);
            }

            updPlr();
        }
    }//GEN-LAST:event_pagarActionPerformed

